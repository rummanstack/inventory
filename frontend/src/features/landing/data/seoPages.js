export function getFeaturePage(slug, language = 'en') {
  const entry = featurePages.find((page) => page.slug === slug);
  if (!entry) return undefined;
  return { slug: entry.slug, image: entry.image, ...(entry[language] || entry.en) };
}

export function getSolutionPage(slug, language = 'en') {
  const entry = solutionPages.find((page) => page.slug === slug);
  if (!entry) return undefined;
  return { slug: entry.slug, image: entry.image, ...(entry[language] || entry.en) };
}

